function gloss(en) {
  const k = String(en || "").trim().toLowerCase();
  if (GLOSS[k]) return GLOSS[k];
  // partial
  for (const [a, b] of Object.entries(GLOSS)) {
    if (k.includes(a) && a.length >= 3) return b;
  }
  return String(en || "").trim();
}

