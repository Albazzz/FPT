function topicOf(question, answer) {
  const t = `${question} ${answer}`.toLowerCase();
  // Order matters: process state / compile before generic "disk/memory"
  // NOTE: avoid matching "which statement" via "which state"
  if (
    /\b(ready|waiting|running|terminated|suspended)\b|process state|which state is|most likely in\?/i.test(
      t
    )
  )
    return "state";
  if (/deadlock/i.test(t)) return "state";
  if (/compiler|interpreter|assembly language|mnemonic|translated before execution|compilation process/i.test(t))
    return "lang";
  if (/bit|byte|kb|1024|2\^n|bcd|hex|octal|two'?s complement|decimal digit/i.test(t)) return "size";
  if (/generation|vlsi|vacuum tube|transistor/i.test(t)) return "gen";
  if (/drop table|sql command|truncate table/i.test(t)) return "sql";
  if (/sql|join|1nf|2nf|3nf|cartesian|transaction|two-phase commit/i.test(t)) return "db";
  if (/circuit switching|packet switching|message switching/i.test(t)) return "switch";
  if (/router|hub|switch|arp|icmp|\bip\b|mac|tcp|udp|network|lan|token|csma|adsl|hdlc|atm|fdm/i.test(t))
    return "net";
  if (/raid|mirror|stripe/i.test(t)) return "raid";
  if (/batch|real-time|centralized|distributed|payroll|reservation/i.test(t)) return "process";
  if (/open-source|middleware|interface design|usability|license|software acts as/i.test(t)) return "soft";
  if (/tapping|scaveng|salami|falsif|crime|attack/i.test(t)) return "crime";
  if (/iso|step|edi|cmmi|common criteria|servlet/i.test(t)) return "std";
  if (/cpu|alu|control unit|pipeline|dma|pc register|instruction cycle|fetch/i.test(t)) return "cpu";
  if (/security|confiden|integrit|availab|auth/i.test(t)) return "sec";
  if (/ram|rom|sram|dram|cache|memory|volatile|non-volatile|storage|hard disk|ssd|auxiliary|register/i.test(t))
    return "mem";
  if (/process management|os management/i.test(t)) return "os";
  return "misc";
}

function normalizeAnswer(ans) {
  let a = String(ans || "").trim();
  // take first alternative before /
  if (a.includes("/") && a.length > 40) {
    /* keep */
  } else if (a.includes(" / ")) {
    a = a.split(" / ")[0].trim();
  } else if (/ or /i.test(a) && a.length < 80) {
    a = a.split(/ or /i)[0].trim();
  }
  // strip trailing parenthetical dumps
  a = a.replace(/\s*\([^)]{40,}\)\s*$/, "").trim();
  return a;
}

function makeOptions(question, correctRaw, seed) {
  const correct = normalizeAnswer(correctRaw);
  const topic = topicOf(question, correct);
  const primary = shuffleInPlace([...(POOLS[topic] || POOLS.misc)], seed);
  const fallback = shuffleInPlace(
    [
      ...POOLS.misc,
      ...POOLS.mem,
      ...POOLS.cpu,
      ...POOLS.os,
      ...POOLS.soft,
    ].filter((x) => !(POOLS[topic] || []).includes(x)),
    seed + 3
  );
  const wrong = [];
  const seen = new Set([correct.toLowerCase()]);

  const tryAdd = (c) => {
    const t = String(c || "").trim();
    if (!t || seen.has(t.toLowerCase())) return false;
    if (t.toLowerCase() === correct.toLowerCase()) return false;
    // too substring-similar
    if (t.length > 3 && correct.toLowerCase().includes(t.toLowerCase())) return false;
    if (correct.length > 3 && t.toLowerCase().includes(correct.toLowerCase())) return false;
    seen.add(t.toLowerCase());
    wrong.push(t);
    return true;
  };

  // numeric distractors (same "topic" of numbers)
  if (/^-?\d+(\.\d+)?$/.test(correct.replace(/,/g, ""))) {
    const n = Number(String(correct).replace(/,/g, ""));
    for (const d of [n + 1, n - 1, n * 2, Math.round(n / 2), 0, 1, 2, 4, 8, 16, 10, 32, 64]) {
      if (!Number.isFinite(d) || d === n) continue;
      tryAdd(String(d));
      if (wrong.length >= 3) break;
    }
  }

  // generation-style
  if (/generation|th generation|st generation|nd generation|rd generation/i.test(correct + question)) {
    for (const g of shuffleInPlace(POOLS.gen.slice(), seed)) tryAdd(g);
  }

  // process states
  if (topic === "state" || /waiting|ready|running|terminated|suspended/i.test(correct)) {
    for (const s of primary) tryAdd(s);
  }

  for (const cand of primary) {
    if (wrong.length >= 3) break;
    tryAdd(cand);
  }
  for (const cand of fallback) {
    if (wrong.length >= 3) break;
    tryAdd(cand);
  }
  let i = 0;
  while (wrong.length < 3) tryAdd(`Phương án nhiễu ${++i}`);

  const opts = shuffleInPlace([correct, wrong[0], wrong[1], wrong[2]], seed + 7);
  const options = {};
  let answer = "A";
  opts.forEach((t, i) => {
    options[LETTERS[i]] = t;
    if (t === correct) answer = LETTERS[i];
  });
  return { options, answer, topic };
}

