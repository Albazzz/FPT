// ── Parse BO 300 ────────────────────────────────────────
function parseBO300() {
  const text = fs.readFileSync(path.join(ROOT, "wedJfe/BO_300_CAU_ON_THI.txt"), "utf8");
  const re = /Cau\s+(\d+)\.\s*([\s\S]*?)(?=Cau\s+\d+\.|={5,}|$)/gi;
  const out = [];
  let m;
  while ((m = re.exec(text))) {
    const num = Number(m[1]);
    const body = m[2].trim();
    const lines = body.split(/\r?\n/).map((l) => l.trim()).filter(Boolean);
    const q = lines[0] || "";
    let ans = "";
    let kw = "";
    for (const line of lines) {
      const am = line.match(/^Đáp án:\s*(.+)$/i);
      if (am) ans = am[1].trim();
      const km = line.match(/^\(Tu khoa[^:]*:\s*(.+)\)$/i);
      if (km) kw = km[1].trim();
    }
    if (!q || !ans) continue;
    out.push({ num, question: q, answer: ans, keywords: kw });
  }
  return out;
}

// ── Parse test.txt (Q + answer + detail) ────────────────
function parseTestTxt() {
  const text = fs.readFileSync(path.join(ROOT, "wedJfe/test.txt"), "utf8");
  const map = new Map(); // question lower -> {answer, detail}
  const re =
    /Câu hỏi\s+\d+:\s*(.+?)\s*\nĐáp án:\s*(.+?)(?=\nCâu hỏi|\nDưới đây|$)/gs;
  let m;
  while ((m = re.exec(text))) {
    const question = m[1].replace(/\s+/g, " ").trim();
    const ansBlock = m[2].replace(/\s+/g, " ").trim();
    const pm = ansBlock.match(/^(.+?)(\s*\((.+)\))?\s*$/);
    const label = (pm ? pm[1] : ansBlock).replace(/\s*\(.+$/, "").trim();
    const detail = pm && pm[3] ? pm[3].trim() : ansBlock;
    map.set(question.toLowerCase(), { question, answer: label, detail });
  }
  return map;
}

