function parseDatajfeMd(text) {
  text = text.replace(/log\s*\n\s*2\s*\n\s*.?\s*\n\s*N/gi, "log2 N");
  const parts = text.split(/(?=(?:^|\n)image(?:_\d+)?\.png)/i);
  const out = [];
  for (const p0 of parts) {
    const p = p0.trim();
    if (!p) continue;
    const mimg = p.match(/^(image(?:_\d+)?\.png)/i);
    if (!mimg) continue;
    const img = mimg[1];
    let key = 1;
    const mk = img.match(/image_(\d+)\.png/i);
    if (mk) key = Number(mk[1]);
    const rest = p.slice(mimg[0].length).trim();
    const lines = rest.split(/\r?\n/);
    let q = null;
    const opts = [];
    let i = 0;
    while (i < lines.length) {
      const line = lines[i].trim();
      if (/^Câu hỏi:|^Cau hoi:/i.test(line)) {
        q = line.replace(/^Câu hỏi:\s*|^Cau hoi:\s*/i, "").trim();
        i++;
        while (i < lines.length && !lines[i].trim()) i++;
        let buf = [];
        while (i < lines.length) {
          const s = lines[i].trim();
          if (/^image(?:_\d+)?\.png/i.test(s)) break;
          if (/^Dưới đây|^Duoi day/i.test(s)) break;
          if (!s) {
            if (buf.length) {
              opts.push(buf.join(" ").replace(/\s+/g, " ").trim());
              buf = [];
            }
            i++;
            continue;
          }
          buf.push(s.replace(/^[a-dA-D]\)\s*/, ""));
          i++;
        }
        if (buf.length) opts.push(buf.join(" ").replace(/\s+/g, " ").trim());
        break;
      }
      i++;
    }
    if (q && opts.length) {
      out.push({
        img,
        key,
        q,
        opts: opts
          .map((o) => (/log\s*2\s*N|log2\s*N/i.test(o) ? "log₂ N" : o))
          .filter(Boolean),
      });
    }
  }
  return out;
}

// ── Upgrade MLN / PRM ───────────────────────────────────
function upgradeExplainGeneric(q, { isViStem = false } = {}) {
  const exp = { ...(q.explanation || {}) };
  const options = q.options || {};
  const corrects = new Set(correctLetters(q));
  const ans = [...corrects][0] || "A";

  if (!exp.optionsVi || !Object.keys(exp.optionsVi).length) {
    exp.optionsVi = { ...options };
  }
  if (!exp.questionVi || !String(exp.questionVi).trim()) {
    exp.questionVi = isViStem ? q.question : viQuestion(q.question);
  }
  if (!exp.answerDisplay) {
    exp.answerDisplay = corrects
      .map((L) => `${L}. ${exp.optionsVi[L] || options[L] || L}`)
      .join(" | ");
  }
  if (!exp.concept || !String(exp.concept).trim()) {
    const src = exp.whyCorrect || exp.answerDisplay || "";
    exp.concept = String(src)
      .replace(/^【Khái niệm】/, "")
      .split(/\n/)[0]
      .replace(/^•\s*/, "")
      .replace(/\s+/g, " ")
      .trim()
      .slice(0, 220);
  }
  if (exp.whyCorrect && !String(exp.whyCorrect).includes("•")) {
    const parts = String(exp.whyCorrect)
      .split(/\n+/)
      .map((s) => s.trim())
      .filter(Boolean);
    exp.whyCorrect = bullets(...(parts.length ? parts : [exp.whyCorrect]));
  }
  exp.whyWrong = exp.whyWrong || {};
  exp.whatIs = exp.whatIs || {};
  Object.keys(options).forEach((L) => {
    exp.whatIs[L] = exp.optionsVi[L] || options[L];
    if (corrects.has(L)) return;
    const old = String(exp.whyWrong[L] || "").trim();
    if (/Là gì\?/i.test(old)) {
      exp.whyWrong[L] = old.startsWith("•") ? old : bullets(old);
      return;
    }
    const reason =
      old ||
      `Sai trong ngữ cảnh câu hỏi này vì không phải đáp án đúng (${[...corrects].join("/")}).`;
    exp.whyWrong[L] = bullets(`Là gì? ${exp.whatIs[L]}`, `Vì sao sai? ${reason}`);
  });
  // strip memoryTip to match promt "không thêm mẹo" — keep if present but optional
  return { ...q, explanation: exp };
}

// ── FE build full ───────────────────────────────────────
function buildFeFull() {
  const out = [];
  let id = 1;

  // TEST: from existing + test.txt details + new distractors
  const prev = loadJsBank("fe");
  const testMap = parseTestTxt();
  const testItems = prev.filter((q) => q.task === "test");
  for (const item of testItems) {
    const hit = testMap.get(String(item.question || "").toLowerCase());
    const correctFrom =
      hit?.answer ||
      item.options?.[item.answer] ||
      Object.values(item.options || {})[0];
    const detail = hit?.detail || item.explanation?.concept || "";
    const { options, answer } = makeOptions(item.question, correctFrom, id * 13);
    const explanation = buildExplain({
      question: item.question,
      options,
      answer,
      detail,
      tip: detail,
      keywords: "",
    });
    // prefer richer questionVi from rules
    if (hit) explanation.questionVi = viQuestion(item.question);
    out.push({
      id: id++,
      task: "test",
      taskLabel: "Test",
      num: id - 1,
      question: item.question,
      options,
      answer,
      alternatives: [],
      explanation,
      meta: { rebuilt: true, originId: item.meta?.originId || item.id },
    });
  }

  // BANK300 from BO file
  const bo = parseBO300();
  if (bo.length !== 300) console.warn("BO count", bo.length);
  for (const row of bo) {
    const { options, answer } = makeOptions(row.question, row.answer, row.num * 17 + 3);
    const tip = row.keywords ? `Ôn: ${row.keywords}` : "";
    const explanation = buildExplain({
      question: row.question,
      options,
      answer,
      detail: `Đáp án: ${normalizeAnswer(row.answer)}.`,
      tip,
      keywords: row.keywords,
    });
    out.push({
      id: id++,
      task: "bank300",
      taskLabel: "300 câu",
      num: row.num,
      question: row.question,
      options,
      answer,
      alternatives: [],
      explanation,
      meta: { rebuilt: true, source: "BO_300" },
    });
  }

  // DATAJFE
  const md = fs.readFileSync(path.join(ROOT, "datajfe.md"), "utf8");
  const blocks = parseDatajfeMd(md);
  let n = 0;
  for (const b of blocks) {
    if (DATAJFE_SKIP.has(b.key)) continue;
    if (!(b.key in DATAJFE_ANSWERS)) continue;
    const ci = DATAJFE_ANSWERS[b.key];
    if (ci < 0 || ci >= b.opts.length) continue;
    const options = {};
    b.opts.forEach((t, i) => {
      options[LETTERS[i]] = t;
    });
    const answer = LETTERS[ci];
    const tip = DATAJFE_TIPS[b.key] || "";
    const explanation = buildExplain({
      question: b.q,
      options,
      answer,
      detail: tip,
      tip,
      keywords: "",
    });
    if (DATAJFE_QVI[b.key]) explanation.questionVi = DATAJFE_QVI[b.key];
    else explanation.questionVi = viQuestion(b.q);
    // better optionsVi for long EN options: short vi tip only for answer
    Object.keys(options).forEach((L) => {
      if (L === answer && tip) {
        explanation.optionsVi[L] = tip.length < 120 ? tip : gloss(options[L]);
      } else {
        explanation.optionsVi[L] = gloss(options[L]);
        // if gloss returned same long text, prefix
        if (explanation.optionsVi[L] === options[L] && options[L].length > 80) {
          explanation.optionsVi[L] = options[L].slice(0, 90) + "…";
        }
      }
    });
    n++;
    out.push({
      id: id++,
      task: "datajfe",
      taskLabel: "datajfe",
      num: n,
      question: b.q,
      options,
      answer,
      alternatives: [],
      explanation,
      meta: { imageKey: b.key, img: b.img, rebuilt: true },
    });
  }

  writeBank("fe", out, {
    tasks: ["test", "bank300", "datajfe"],
    note: "full upgrade: BO_300 rebuild + test distractors + datajfe",
  });
  return out.length;
}

// ── JIT polish ──────────────────────────────────────────
function upgradeJit() {
  const qs = loadJsBank("jit").map((q) => {
    const u = upgradeExplainGeneric(q, { isViStem: false });
    const exp = u.explanation;
    if (!exp.questionVi) exp.questionVi = q.explanation?.questionVi || "";
    exp.concept = String(exp.concept || "")
      .replace(/^•\s*/, "")
      .trim();
    const ans = q.answer;
    const ansVi = exp.optionsVi?.[ans] || q.options?.[ans] || ans;
    const lines = String(exp.whyCorrect || "")
      .split("\n")
      .map((s) => s.replace(/^•\s*/, "").trim())
      .filter(Boolean);
    const head = `Chọn ${ans} — ${ansVi}.`;
    const rest = lines.filter((l) => !/^Chọn\s+[A-Z]\s*[—\-]/.test(l));
    exp.whyCorrect = bullets(head, ...rest.slice(0, 4));
    // ensure every wrong option structured
    Object.keys(q.options || {}).forEach((L) => {
      if (L === ans) return;
      const wi = exp.whatIs?.[L] || exp.optionsVi?.[L] || q.options[L];
      const old = String(exp.whyWrong?.[L] || "");
      if (!/Vì sao sai\?/i.test(old)) {
        exp.whyWrong[L] = bullets(
          `Là gì? ${wi}`,
          `Vì sao sai? Sai trong ngữ cảnh câu hỏi này vì cần «${ansVi}», không phải «${exp.optionsVi?.[L] || q.options[L]}».`
        );
      }
    });
    return { ...u, explanation: exp };
  });
  writeBank("jit", qs, { tasks: ["quiz", "sample", "pt"] });
}

