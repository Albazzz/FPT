function feExplain({ question, options, answer, tip, detail, questionVi }) {
  const ans = answer;
  const ansText = options[ans];
  const detailClean = String(detail || "")
    .replace(/^Đáp án:\s*/i, "")
    .trim();

  const concept = tip
    ? tip
    : detailClean
      ? detailClean.length > 220
        ? detailClean.slice(0, 220) + "…"
        : detailClean
      : `${ansText} là đáp án đúng cho câu hỏi.`;

  const whyCorrect = bullets(
    `Chọn ${ans} — ${ansText}.`,
    detailClean && detailClean !== ansText ? detailClean : null,
    tip && tip !== detailClean && tip !== concept ? tip : null
  );

  const whatIs = {};
  const whyMap = {};
  Object.keys(options).forEach((L) => {
    whatIs[L] = options[L];
    if (L === ans) return;
    let reason = `Sai trong ngữ cảnh câu hỏi này vì cần «${ansText}», không phải «${options[L]}».`;
    for (const rule of FE_CONTRAST) {
      if (rule.ifAns.test(ansText + " " + (detailClean || "")) && rule.ifOpt.test(options[L])) {
        reason = rule.why;
        break;
      }
    }
    // numeric garbage / cross-question junk often short weird tokens
    if (/^\d+(\.\d+)?$/.test(options[L].trim())) {
      reason =
        "Sai trong ngữ cảnh câu hỏi này — giá trị số không liên quan tới khái niệm được hỏi.";
    }
    whyMap[L] = reason;
  });

  const whyWrong = buildWhyWrongStructured(options, ans, whatIs, whyMap);
  const optionsVi = { ...options };

  return {
    questionVi: questionVi || "",
    optionsVi,
    answerDisplay: `${ans}. ${ansText}`,
    concept,
    whyCorrect,
    whyWrong,
    whatIs,
  };
}

function loadWedQuiz() {
  const raw = fs.readFileSync(path.join(ROOT, "wedJfe/js/data-quiz.js"), "utf8");
  const m = raw.match(/window\.QUIZ_DATA\s*=\s*(\[[\s\S]*\])\s*;?\s*$/m);
  if (!m) throw new Error("Cannot parse wedJfe data-quiz.js");
  // eslint-disable-next-line no-new-func
  return Function(`"use strict"; return (${m[1]});`)();
}

function rebuildFromExistingFe(item, task) {
  const rawOpts = item.options || [];
  const cleaned = rawOpts.map((o) => cleanOptionText(o));
  // recover explain from polluted correct option or explanation field
  const ci = item.correctIndex;
  const rawCorrect = rawOpts[ci] || "";
  const { label: correctLabel, detail: fromOpt } = splitParenExplain(rawCorrect);
  const fromExp = splitParenExplain(item.explanation || item.answerText || "");
  const detail = fromOpt || fromExp.detail || fromExp.label || "";
  const options = {};
  cleaned.forEach((t, i) => {
    options[LETTERS[i]] = t || `Option ${LETTERS[i]}`;
  });
  // ensure correct option uses clean short label
  if (correctLabel) options[LETTERS[ci]] = correctLabel;

  const answer = LETTERS[ci];
  // detect scrambled: wrong options look numeric while Q is conceptual, etc. — still keep
  const explanation = feExplain({
    question: item.question,
    options,
    answer,
    tip: "",
    detail,
    questionVi: "",
  });
  // If detail is good, put as concept
  if (detail) {
    explanation.concept = detail.length > 200 ? detail.slice(0, 200) + "…" : detail;
  }
  return {
    question: item.question,
    options,
    answer,
    explanation,
    originId: item.id,
  };
}

function buildFe() {
  const existing = loadWedQuiz();
  let id = 1;
  const out = [];

  // 1) test + bank300 from existing (cleaned)
  for (const item of existing) {
    const src = item.source || "test";
    if (src === "datajfe") continue; // rebuild from md
    const task = src === "bank300" ? "bank300" : "test";
    const taskLabel = task === "bank300" ? "300 câu" : "Test";
    const rebuilt = rebuildFromExistingFe(item, task);
    // Skip empty/broken
    if (!rebuilt.question || Object.keys(rebuilt.options).length < 2) continue;
    out.push({
      id: id++,
      task,
      taskLabel,
      num: id - 1,
      question: rebuilt.question,
      options: rebuilt.options,
      answer: rebuilt.answer,
      alternatives: [],
      explanation: rebuilt.explanation,
      meta: { originId: rebuilt.originId, cleaned: true },
    });
  }

  // 2) datajfe from markdown (gold source for options)
  const md = fs.readFileSync(path.join(ROOT, "datajfe.md"), "utf8");
  const blocks = parseDatajfeMd(md);
  let n = 0;
  for (const b of blocks) {
    if (DATAJFE_SKIP.has(b.key)) continue;
    if (!(b.key in DATAJFE_ANSWERS)) {
      console.warn("[datajfe] no answer map for key", b.key);
      continue;
    }
    const ci = DATAJFE_ANSWERS[b.key];
    if (ci < 0 || ci >= b.opts.length) {
      console.warn("[datajfe] bad index", b.key, ci, b.opts.length);
      continue;
    }
    const options = optionsFromArray(b.opts);
    const answer = LETTERS[ci];
    const tip = DATAJFE_TIPS[b.key] || "";
    const explanation = feExplain({
      question: b.q,
      options,
      answer,
      tip,
      detail: tip,
      questionVi: "", // EN exam style — VI filled lightly via tip in concept
    });
    // bilingual-ish: put tip as concept + questionVi empty; optionsVi = options
    explanation.questionVi = ""; // keep EN as primary for FE
    n++;
    out.push({
      id: id++,
      task: "datajfe",
      taskLabel: "datajfe",
      num: n,
      question: b.q,
      options,
      answer,
      alternatives: [],
      explanation,
      meta: { imageKey: b.key, img: b.img },
    });
  }

  writeBank("fe", out, {
    tasks: ["test", "bank300", "datajfe"],
    note: "test/bank300: options cleaned from legacy; datajfe rebuilt from datajfe.md",
  });
}

