// ─── JIT / jl ──────────────────────────────────────────
function extractJsArray(html, name) {
  const markers = [`const ${name} =`, `const ${name}=`];
  let i = -1;
  for (const m of markers) {
    i = html.indexOf(m);
    if (i >= 0) break;
  }
  if (i < 0) throw new Error(`Cannot find ${name}`);
  const start = html.indexOf("[", i);
  let depth = 0;
  let end = -1;
  for (let j = start; j < html.length; j++) {
    const c = html[j];
    if (c === "[") depth++;
    else if (c === "]") {
      depth--;
      if (depth === 0) {
        end = j;
        break;
      }
    }
  }
  // eslint-disable-next-line no-new-func
  return Function(`"use strict"; return (${html.slice(start, end + 1)});`)();
}

function parseCvList(cv) {
  const opts = {};
  const optsVi = {};
  (cv || []).forEach((line, i) => {
    const L = LETTERS[i];
    const raw = String(line);
    const m = raw.match(/^([A-Fa-f])[\.\)\:]\s*(.*)$/);
    const body = m ? m[2] : stripLetterPrefix(raw);
    // SAMPLE often "収集（しゅうしゅう）：Thu thập"
    const parts = body.split(/：|:\s*/);
    if (parts.length >= 2 && /[\u3040-\u30ff\u4e00-\u9fff]/.test(parts[0])) {
      opts[L] = parts[0].trim();
      optsVi[L] = parts.slice(1).join(":").trim();
    } else {
      opts[L] = body.trim();
      optsVi[L] = body.trim();
    }
  });
  return { opts, optsVi };
}

function isMostlyJapanese(s) {
  const t = String(s || "");
  const jp = (t.match(/[\u3040-\u30ff\u4e00-\u9fff]/g) || []).length;
  return jp >= 8 && jp >= t.length * 0.25;
}

function rebuildJitExplain(q, tr, task) {
  const answer = String(q.a || "A").toUpperCase();
  const { optsVi: fromC } = parseCvList((tr && tr.cv) || q.c.map((x) => x));
  const options = optionsFromArray(q.c);
  const optionsVi = {};
  Object.keys(options).forEach((L) => {
    if (tr && tr.cv) {
      const line = tr.cv[LETTERS.indexOf(L)] || "";
      const body = stripLetterPrefix(line);
      const col = body.split(/：|:\s*/);
      if (col.length >= 2) optionsVi[L] = col[col.length - 1].trim();
      else optionsVi[L] = body;
    } else if (fromC[L]) optionsVi[L] = fromC[L];
    else optionsVi[L] = options[L];
  });

  const qv = (tr && tr.qv) || "";
  const whyRaw = (tr && tr.why) || "";
  const exJp = (q.ex || "").trim();

  let whyCorrectCore = "";
  const mWhy = whyRaw.match(/Lý do:\s*(.+?)(?:\s+Khớp|\s+Đây là|\s+So với|$)/s);
  if (mWhy) whyCorrectCore = mWhy[1].trim();
  else if (exJp) whyCorrectCore = exJp;
  else whyCorrectCore = whyRaw;

  whyCorrectCore = whyCorrectCore
    .replace(/Khớp kiến thức giáo trình[^.]*(?:\.|$)/g, "")
    .replace(/So với lựa chọn khác:.*$/s, "")
    .replace(/✓\s*Chọn\s*[A-D]\s*[—\-].*?Lý do:\s*/i, "")
    .trim();

  const ansVi = optionsVi[answer] || options[answer] || answer;
  const ansJp = options[answer] || "";

  // Concept: always Vietnamese-first (promt.md)
  const concept = bullets(
    `${ansVi}${ansJp && ansJp !== ansVi ? ` (${ansJp})` : ""} là đáp án đúng.`
  ).replace(/^•\s*/, "");

  const whyLines = [];
  whyLines.push(`Chọn ${answer} — ${ansVi}.`);
  if (qv) whyLines.push(`Câu hỏi hỏi: ${qv}`);
  if (whyCorrectCore && !isMostlyJapanese(whyCorrectCore)) {
    whyLines.push(whyCorrectCore);
  } else if (exJp) {
    whyLines.push(`Theo giáo trình (JP): ${exJp}`);
  } else if (whyCorrectCore) {
    whyLines.push(`Ghi chú: ${whyCorrectCore}`);
  }
  // Detect "chọn phát biểu SAI"
  if (/sai|正しくない|正しくないものを/i.test(q.q + qv)) {
    whyLines.push("Đây là câu tìm phát biểu SAI: đáp án là nội dung không đúng.");
  }
  const whyCorrect = bullets(...whyLines);

  const whyMap = {};
  const so = whyRaw.match(/So với lựa chọn khác:\s*(.+)$/s);
  if (so) {
    so[1].split("|").forEach((chunk) => {
      const m = chunk.trim().match(/^([A-D])\s*(.+)$/i);
      if (!m) return;
      const L = m[1].toUpperCase();
      let rest = m[2]
        .replace(/^sai\/không khớp:\s*/i, "")
        .replace(
          /^đúng\/không phải đáp án vì đây không phải phát biểu sai\s*/i,
          "Đúng nhưng không phải đáp án (câu hỏi tìm phát biểu SAI). "
        )
        .trim();
      // drop if rest is only the option text again
      if (rest && rest !== optionsVi[L] && rest !== options[L]) whyMap[L] = rest;
    });
  }

  const whatIs = {};
  Object.keys(options).forEach((L) => {
    const vi = optionsVi[L] || options[L];
    const jp = options[L];
    whatIs[L] = jp && jp !== vi ? `${vi} (JP: ${jp})` : vi;
  });

  const whyWrong = {};
  Object.keys(options).forEach((L) => {
    if (L === answer) return;
    const wi = whatIs[L];
    let reason = whyMap[L];
    if (!reason || reason === optionsVi[L] || reason === options[L]) {
      reason = `Sai trong ngữ cảnh câu hỏi này vì cần «${ansVi}», không phải «${optionsVi[L] || options[L]}».`;
    }
    whyWrong[L] = bullets(`Là gì? ${wi}`, `Vì sao sai? ${reason}`);
  });

  return {
    options,
    explanation: {
      questionVi: qv,
      optionsVi,
      answerDisplay: `${answer}. ${ansVi}`,
      concept,
      whyCorrect,
      whyWrong,
      whatIs,
    },
  };
}

function buildJit() {
  const html = fs.readFileSync(path.join(ROOT, "jl/index.html"), "utf8");
  const QUIZ = extractJsArray(html, "QUIZ");
  const SAMPLE = extractJsArray(html, "SAMPLE");
  const PT = extractJsArray(html, "PT");
  const QUIZ_TR = extractJsArray(html, "QUIZ_TR");
  const SAMPLE_TR = extractJsArray(html, "SAMPLE_TR");
  const PT_TR = extractJsArray(html, "PT_TR");

  let id = 1;
  const out = [];
  const packs = [
    { task: "quiz", label: "Quiz", bank: QUIZ, tr: QUIZ_TR },
    { task: "sample", label: "Sample", bank: SAMPLE, tr: SAMPLE_TR },
    { task: "pt", label: "Quiz PT", bank: PT, tr: PT_TR },
  ];
  for (const pack of packs) {
    pack.bank.forEach((q, idx) => {
      const tr = pack.tr[idx] || null;
      const rebuilt = rebuildJitExplain(q, tr, pack.task);
      out.push({
        id: id++,
        task: pack.task,
        taskLabel: pack.label,
        num: idx + 1,
        chapterHint: q.m,
        question: q.q,
        options: rebuilt.options,
        answer: String(q.a || "A").toUpperCase(),
        alternatives: [],
        explanation: rebuilt.explanation,
        meta: { originEx: q.ex || "" },
      });
    });
  }
  writeBank("jit", out, { tasks: ["quiz", "sample", "pt"] });
}

