/**
 * Build unified quiz data into quiz/data/*.js
 * Sources: MLN, PRM, jl (index.html), wedJfe (+ datajfe.md rebuild)
 *
 * Schema (PRM-compatible + task):
 * {
 *   id, task, taskLabel, num?,
 *   question, options: {A,B,C,...}, answer | answers[],
 *   alternatives?, explanation: {
 *     questionVi?, optionsVi?, answerDisplay?,
 *     concept?, whyCorrect?, whyWrong?: {A: string},
 *     whatIs?: {A: string},  // "Là gì?" per option
 *     memoryTip?  // optional; promt.md prefers none — keep if high quality
 *   }
 * }
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "../..");
const OUT = path.resolve(__dirname, "../data");

const LETTERS = "ABCDEFGHIJKLMNOPQRSTUVWXYZ".split("");

function ensureDir(d) {
  fs.mkdirSync(d, { recursive: true });
}

function writeBank(key, questions, meta = {}) {
  ensureDir(OUT);
  const payload = {
    subject: key,
    generatedAt: new Date().toISOString(),
    count: questions.length,
    ...meta,
    questions,
  };
  const jsonPath = path.join(OUT, `${key}.json`);
  const jsPath = path.join(OUT, `${key}.js`);
  fs.writeFileSync(jsonPath, JSON.stringify(payload, null, 0), "utf8");
  const js =
    `// Auto-generated by quiz/tools/build_all.mjs — do not edit by hand\n` +
    `window.QUIZ_DATA = window.QUIZ_DATA || {};\n` +
    `window.QUIZ_DATA[${JSON.stringify(key)}] = ${JSON.stringify(questions)};\n`;
  fs.writeFileSync(jsPath, js, "utf8");
  console.log(`[ok] ${key}: ${questions.length} → data/${key}.js`);
}

function stripLetterPrefix(s) {
  return String(s || "")
    .replace(/^[A-Fa-f][\.\)\:]\s*/, "")
    .trim();
}

function optionsFromArray(arr) {
  const out = {};
  (arr || []).forEach((t, i) => {
    out[LETTERS[i]] = stripLetterPrefix(t);
  });
  return out;
}

function optionsFromObject(obj) {
  const out = {};
  Object.keys(obj || {})
    .sort()
    .forEach((k) => {
      out[k.toUpperCase()] = String(obj[k] ?? "");
    });
  return out;
}

/** Pull short label + long parenthetical as explain text */
function splitParenExplain(text) {
  const s = String(text || "").trim();
  const m = s.match(/^(.*?)(\s*[\(（](.+)[\)）])\s*$/);
  if (m && m[1].trim().length >= 1 && m[3].trim().length > 8) {
    return { label: m[1].trim().replace(/[.,;:]+$/, ""), detail: m[3].trim() };
  }
  // "Đáp án: X (detail)"
  const m2 = s.match(/^Đáp án:\s*(.+?)(\s*\((.+)\))?\s*\.?$/i);
  if (m2) {
    return {
      label: m2[1].replace(/\s*\(.+$/, "").trim(),
      detail: (m2[3] || m2[1] || "").trim(),
    };
  }
  return { label: s, detail: "" };
}

function cleanOptionText(s) {
  const { label } = splitParenExplain(s);
  // if still very long polluted, take first clause before " - " with long vi
  if (label.length > 120 && label.includes(" - ")) {
    return label.split(" - ")[0].trim();
  }
  return label || String(s || "").trim();
}

function bullets(...lines) {
  return lines
    .filter(Boolean)
    .map((l) => String(l).replace(/^\s*[•\-\*]\s*/, "").trim())
    .filter(Boolean)
    .map((l) => `• ${l}`)
    .join("\n");
}

function buildWhyWrongStructured(opts, answerLetter, whatIs, whyMap) {
  const wrong = {};
  Object.keys(opts).forEach((L) => {
    if (L === answerLetter) return;
    const name = opts[L];
    const wi = (whatIs && whatIs[L]) || `Khái niệm/phương án: ${name}`;
    const why =
      (whyMap && whyMap[L]) ||
      `Sai trong ngữ cảnh câu hỏi này vì không phải đáp án đúng.`;
    wrong[L] = bullets(`Là gì? ${wi}`, `Vì sao sai? ${why}`);
  });
  return wrong;
}

// ─── PRM ───────────────────────────────────────────────
function buildPrm() {
  const raw = JSON.parse(
    fs.readFileSync(path.join(ROOT, "PRM/questions.json"), "utf8")
  );
  const qs = raw.map((q, i) => {
    const task = q.exam === "re" ? "re" : "fe";
    const taskLabel =
      q.examLabel || (task === "re" ? "SP26-B5-FE" : "SP26-FE");
    const exp = q.explanation || {};
    const explanation = {
      questionVi: exp.questionVi || "",
      optionsVi: exp.optionsVi || {},
      answerDisplay: exp.answerDisplay || "",
      concept: exp.concept || "",
      whyCorrect: exp.whyCorrect || "",
      whyWrong: exp.whyWrong || {},
      memoryTip: exp.memoryTip || undefined,
    };
    // Flex: ensure concept exists when we have whyCorrect
    if (!explanation.concept && explanation.whyCorrect) {
      const first = String(explanation.whyCorrect).split(/\n/)[0];
      explanation.concept = first
        .replace(/^【Khái niệm】/, "")
        .replace(/^•\s*/, "")
        .slice(0, 220);
    }
    return {
      id: q.id != null ? Number(q.id) : i + 1,
      task,
      taskLabel,
      num: q.num != null ? q.num : q.id,
      question: q.question,
      options: optionsFromObject(q.options),
      answer: q.answer,
      answers: q.answers,
      alternatives: q.alternatives || [],
      explanation,
      source: q.source || taskLabel,
    };
  });
  writeBank("prm", qs, { tasks: ["fe", "re"] });
}

