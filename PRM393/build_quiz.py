def Q(
    source: str,
    num: int,
    question: str,
    options: dict[str, str],
    answer: str,
    question_vi: str,
    options_vi: dict[str, str],
    concept: str,
    why_correct: str,
    why_wrong: dict[str, str],
    tip: str,
) -> dict:
    letters = ["A", "B", "C", "D"]
    ans = answer.upper()
    disp = f"{ans}. {options_vi.get(ans) or options.get(ans, '')}"
    why = f"【Khái niệm】{concept}\n\n【Vì sao đúng】{why_correct}"
    # exam code for quiz filter: b5 -> tab "re" (B5), other FE -> "fe"
    exam_code = "re" if "B5" in source else "fe"
    return {
        "source": source,
        "num": num,
        "exam": exam_code,
        "examLabel": source,
        "question": question,
        "options": {k: options[k] for k in letters if k in options},
        "answer": ans,
        "alternatives": [],
        "explanation": {
            "questionVi": question_vi,
            "optionsVi": {k: options_vi[k] for k in letters if k in options_vi},
            "answerDisplay": disp,
            "concept": concept,
            "whyCorrect": why,
            "memoryTip": tip,
            "whyWrong": {k: why_wrong[k] for k in letters if k in why_wrong and k != ans},
        },
    }


