def main() -> None:
    items = B5 + FE
    for i, q in enumerate(items, start=1):
        q["id"] = i

    # de-dup check
    keys = [(q["source"], q["num"]) for q in items]
    assert len(keys) == len(set(keys)), "duplicate source/num"

    json_path = OUT_DIR / "questions.json"
    js_path = OUT_DIR / "questions.js"
    json_path.write_text(
        json.dumps(items, ensure_ascii=False, indent=2) + "\n", encoding="utf-8"
    )
    js_path.write_text(
        "window.QUIZ_QUESTIONS = "
        + json.dumps(items, ensure_ascii=False, indent=2)
        + ";\n",
        encoding="utf-8",
    )

    # human-readable data.txt style
    lines: list[str] = [
        "PRM393 Quiz Data — SP26 B5 FE (48) + SP26 FE (50) = 98 câu",
        "Mỗi câu: EN + VI + đáp án + khái niệm + giải thích",
        "",
    ]
    for q in items:
        exp = q["explanation"]
        lines.append(f"===== [{q['id']}] {q['examLabel']} #{q['num']} =====")
        lines.append(q["question"])
        lines.append(f"(VI) {exp['questionVi']}")
        for L, t in q["options"].items():
            mark = " ✓" if L == q["answer"] else ""
            lines.append(f"  {L}. {t}{mark}")
            lines.append(f"     VI: {exp['optionsVi'].get(L, '')}")
        lines.append(f"Đáp án: {exp['answerDisplay']}")
        lines.append(exp["whyCorrect"])
        lines.append(f"Mẹo: {exp['memoryTip']}")
        for L, w in exp["whyWrong"].items():
            lines.append(f"  Sai {L}: {w}")
        lines.append("")

    (OUT_DIR / "quiz_data.txt").write_text("\n".join(lines), encoding="utf-8")

    print(f"Wrote {len(items)} questions")
    print(f"  {json_path}")
    print(f"  {js_path}")
    print(f"  {OUT_DIR / 'quiz_data.txt'}")
    print(f"  B5={sum(1 for q in items if q['source']=='SP26-B5-FE')} FE={sum(1 for q in items if q['source']=='SP26-FE')}")


if __name__ == "__main__":
    main()
